import { mkdir, readFile, readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

import ts from "typescript";

const normalizePath = (value) => value.split(path.sep).join("/");

export default function gotifyPluginRegistryPlugin(configuredPlugins) {
	let rootDir = process.cwd();
	let running = null;

	const run = async () => {
		running ??= generatePluginRegistry(rootDir, configuredPlugins).finally(() => {
			running = null;
		});
		return running;
	};

	const isRelevantFile = (file) => {
		const normalizedFile = normalizePath(file);
		const normalizedRoot = normalizePath(rootDir);
		return normalizedFile.startsWith(`${normalizedRoot}/plugins/`);
	};

	return {
		name: "gotify-plugin-registry",
		enforce: "pre",
		async configResolved(config) {
			rootDir = config.root;
			await run();
		},
		configureServer(server) {
			server.watcher.add([path.join(rootDir, "vite.config.ts"), path.join(rootDir, "plugins")]);
		},
		async handleHotUpdate(ctx) {
			if (isRelevantFile(ctx.file)) {
				await run();
			}
		},
	};
}

export async function generatePluginRegistry(rootDir, configuredPlugins) {
	const context = createContext(rootDir);

	const workspacePackages = await loadWorkspacePackages(context.pluginsDir);
	const { compatibilityDate, compatibilityFlags } = await readWorkerCompatibility(context.wranglerPath);

	const pluginDefinitions = [];
	for (const plugin of configuredPlugins) {
		validatePluginConfig(plugin);

		const packageRoot = typeof plugin.packageRoot === "string" ? plugin.packageRoot : fileURLToPath(plugin.packageRoot);
		const sourceEntrypoint = path.resolve(packageRoot, plugin.entrypoint);
		const modules = await collectModules(sourceEntrypoint, context.pluginsDir, workspacePackages);

		pluginDefinitions.push({
			manifest: plugin.manifest,
			defaultConfig: plugin.defaultConfig,
			entrypointName: plugin.entrypointName ?? "PluginWorker",
			workerCode: {
				compatibilityDate,
				compatibilityFlags,
				mainModule: toModuleKey(context.pluginsDir, sourceEntrypoint),
				modules,
			},
		});
	}

	const output = `// Generated by scripts/generate-plugin-registry.mjs. Do not edit by hand.
export const configuredPlugins = ${JSON.stringify(pluginDefinitions, null, "\t")} as const;
`;

	await mkdir(path.dirname(context.outputPath), { recursive: true });
	await writeFile(context.outputPath, output);
}

function createContext(rootDir) {
	return {
		rootDir,
		pluginsDir: path.join(rootDir, "plugins"),
		outputPath: path.join(rootDir, "src", "worker", "generated", "plugin-registry.ts"),
		wranglerPath: path.join(rootDir, "wrangler.jsonc"),
	};
}

function formatDiagnostics(diagnostics) {
	return ts.formatDiagnosticsWithColorAndContext(diagnostics, {
		getCanonicalFileName: (fileName) => fileName,
		getCurrentDirectory: () => process.cwd(),
		getNewLine: () => "\n",
	});
}

function validatePluginConfig(plugin) {
	if (!plugin || typeof plugin !== "object") {
		throw new Error("the Vite plugin must receive an array of plugin descriptors");
	}
	if (!plugin.packageRoot || !plugin.entrypoint || !plugin.manifest || typeof plugin.defaultConfig !== "string") {
		throw new Error("plugin descriptor must include packageRoot, entrypoint, manifest, and defaultConfig");
	}
}

async function readWorkerCompatibility(wranglerPath) {
	const wranglerText = await readFile(wranglerPath, "utf8");
	const dateMatch = wranglerText.match(/"compatibility_date"\s*:\s*"([^"]+)"/u);
	const flagsMatch = wranglerText.match(/"compatibility_flags"\s*:\s*\[([^\]]*)\]/u);

	return {
		compatibilityDate: dateMatch?.[1] ?? new Date().toISOString().slice(0, 10),
		compatibilityFlags: flagsMatch ? [...flagsMatch[1].matchAll(/"([^"]+)"/gu)].map((match) => match[1]) : [],
	};
}

function toRuntimePath(pluginsDir, sourcePath) {
	const relativePath = path.relative(pluginsDir, sourcePath);
	if (relativePath.startsWith("..")) {
		throw new Error(`plugin entrypoint must live under plugins/: ${sourcePath}`);
	}
	return path.join(pluginsDir, replaceExtension(relativePath, ".js"));
}

function replaceExtension(filePath, extension) {
	return filePath.replace(/\.[^.]+$/u, extension);
}

function toModuleKey(pluginsDir, filePath) {
	return `./${replaceExtension(path.relative(pluginsDir, filePath), ".js").split(path.sep).join("/")}`;
}

async function collectModules(entrypointPath, pluginsDir, workspacePackages) {
	const modules = {};
	const visited = new Set();

	await visit(entrypointPath, toModuleKey(pluginsDir, entrypointPath));
	return modules;

	async function visit(modulePath, moduleKey) {
		const normalizedPath = path.normalize(modulePath);
		const visitKey = `${moduleKey}::${normalizedPath}`;
		if (visited.has(visitKey)) {
			return;
		}
		visited.add(visitKey);

		let source = await readFile(normalizedPath, "utf8");
		const replacements = [];

		for (const specifier of findImports(source)) {
			const resolved = resolveImportedModule(path.dirname(normalizedPath), specifier, pluginsDir, workspacePackages);
			if (!resolved) {
				continue;
			}

			const replacementSpecifier = buildRelativeModuleSpecifier(moduleKey, resolved.moduleKey);
			if (replacementSpecifier !== specifier) {
				replacements.push([specifier, replacementSpecifier]);
			}
			await visit(resolved.filePath, resolved.moduleKey);
		}

		source = applySpecifierReplacements(source, replacements);
		modules[moduleKey] = { js: transpileModule(normalizedPath, source) };
	}
}

function findImports(source) {
	const matches = [
		...source.matchAll(/\b(?:import|export)\b(?:\s+type)?(?:[\s\w{},*$]*?\s+from\s+)?["']([^"']+)["']/gu),
		...source.matchAll(/\bimport\(\s*["']([^"']+)["']\s*\)/gu),
	];
	return matches.map((match) => match[1]).filter(Boolean);
}

function resolveImportedModule(baseDir, specifier, pluginsDir, workspacePackages) {
	if (specifier.startsWith(".")) {
		const filePath = resolveModulePath(baseDir, specifier);
		return {
			filePath,
			moduleKey: toModuleKey(pluginsDir, filePath),
		};
	}

	if (specifier.startsWith("@gotify/")) {
		const filePath = resolveWorkspaceModulePath(specifier, pluginsDir, workspacePackages);
		return {
			filePath,
			moduleKey: toModuleKey(pluginsDir, filePath),
		};
	}

	return null;
}

function resolveModulePath(baseDir, specifier) {
	const resolved = path.resolve(baseDir, specifier);
	if (path.extname(resolved)) {
		return resolved;
	}
	for (const extension of [".ts", ".mts", ".tsx", ".js", ".mjs"]) {
		const candidate = `${resolved}${extension}`;
		if (ts.sys.fileExists(candidate)) {
			return candidate;
		}
	}
	return `${resolved}.ts`;
}

function buildRelativeModuleSpecifier(fromModuleKey, toModuleKeyValue) {
	const fromDir = path.posix.dirname(fromModuleKey);
	let relativePath = path.posix.relative(fromDir, toModuleKeyValue);
	if (!relativePath.startsWith(".")) {
		relativePath = `./${relativePath}`;
	}
	return relativePath;
}

function applySpecifierReplacements(source, replacements) {
	let nextSource = source;
	for (const [fromSpecifier, toSpecifier] of replacements) {
		nextSource = nextSource.replaceAll(`"${fromSpecifier}"`, `"${toSpecifier}"`);
		nextSource = nextSource.replaceAll(`'${fromSpecifier}'`, `'${toSpecifier}'`);
	}
	return nextSource;
}

function resolveWorkspaceModulePath(specifier, pluginsDir, workspacePackages) {
	const { packageName, subpath } = splitWorkspaceSpecifier(specifier);
	const workspacePackage = workspacePackages.get(packageName);
	if (!workspacePackage) {
		throw new Error(`unknown workspace package import: ${specifier}`);
	}

	const exportKey = subpath ? `./${subpath}` : ".";
	const exportTarget = workspacePackage.exports[exportKey];
	if (typeof exportTarget !== "string") {
		throw new Error(`workspace package '${packageName}' does not export '${exportKey}'`);
	}

	const sourcePath = path.resolve(workspacePackage.rootDir, exportTarget);
	if (!ts.sys.fileExists(sourcePath)) {
		throw new Error(`workspace package '${packageName}' export '${exportKey}' points to missing file '${sourcePath}'`);
	}
	return sourcePath;
}

function splitWorkspaceSpecifier(specifier) {
	const [scope, name, ...segments] = specifier.split("/");
	return {
		packageName: `${scope}/${name}`,
		subpath: segments.join("/"),
	};
}

async function loadWorkspacePackages(pluginsDir) {
	const packageRoots = await listDirectories(pluginsDir);
	const packages = new Map();

	for (const packageRoot of packageRoots) {
		const packageJsonPath = path.join(packageRoot, "package.json");
		const packageJson = JSON.parse(await readFile(packageJsonPath, "utf8"));
		packages.set(packageJson.name, {
			rootDir: packageRoot,
			pluginsDir,
			exports: packageJson.exports ?? {},
		});
	}

	return packages;
}

async function listDirectories(rootDir) {
	const entries = await readdir(rootDir, { withFileTypes: true });
	return entries.filter((entry) => entry.isDirectory()).map((entry) => path.join(rootDir, entry.name));
}

function transpileModule(filePath, source) {
	const result = ts.transpileModule(source, {
		fileName: filePath,
		reportDiagnostics: true,
		compilerOptions: {
			target: ts.ScriptTarget.ES2022,
			module: ts.ModuleKind.ESNext,
			moduleResolution: ts.ModuleResolutionKind.Bundler,
			rewriteRelativeImportExtensions: true,
			verbatimModuleSyntax: true,
		},
	});

	if (result.diagnostics?.length) {
		throw new Error(formatDiagnostics(result.diagnostics));
	}

	return result.outputText;
}
